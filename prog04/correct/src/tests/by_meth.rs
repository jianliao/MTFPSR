use crate::load;
use triemap::TrieMap;

fn mk_test(
    by_meth: fn(&TrieMap<u64>, &str, usize) -> Option<String>,
    by_meth_str: &str,
    model: &TrieMap<u64>,
    word: &str,
    dist: usize,
    ans: Option<&str>,
) {
    let res = by_meth(model, word, dist);
    let res = (&res).as_ref().map(String::as_str);
    assert_eq!(ans, res, "{}(&model, {:?}, {})", by_meth_str, word, dist)
}

pub fn mk_tests<I>(
    by_meth: fn(&TrieMap<u64>, &str, usize) -> Option<String>,
    by_meth_str: &str,
    i: I,
) where
    I: std::slice::SliceIndex<
        [(&'static str, usize, Option<&'static str>)],
        Output = [(&'static str, usize, Option<&'static str>)],
    >,
{
    let corpus = load::from_btcz("assets/big_model.btc.z").unwrap();
    for (w, d, ans) in (&TESTS[i]).iter() {
        mk_test(by_meth, by_meth_str, &corpus, *w, *d, *ans);
    }
}

